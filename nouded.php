	<h2>Teema </h2>

	<p>Kliendiks on personaliotsingu firma, kes soovib uut CRM süsteemi, mis hakkab haldama konkursse, inimeste ankeete, klientide ankeete, inimeste teste, sündmuste kalendrit, konkursside jaoks vajalike toiminguid.</p>

	<h2>Nõuded</h2>

	<ol>
		<h4>Funktsionaalsed</h4>
		
		<li>Mis toiminguid hakatakse süsteemis tegema? Selgitus: et disainida vastav funktsionaalsus.</li>
		<li>Mis info hakkab süsteemis olema? Selgitus: et disainida vastavad andmestruktuurid ja valida sobivad lahendused.</li>
		<li>Kas süsteemi on vaja siduda/liidestada teiste süsteemidega? Selgitus: et aru saada info liikumisest süsteemi väliselt.</li>
		<li>Mida peab leidma otsinguga? Selgitus: otsingumootori projekteerimise jaoks, kas otsida ainult nimede/kontaktide/ametite järgi või, näiteks, filtreerida ka konkreetsete testitulemuste järgi.</li>
		<li>Mis on süsteemi kasutajate rollid? Selgitus, kas tuleb ainult administraator ja kõik muud või tulevad erinevad rollid kus igal rollil oma õigused. </li>
		<li>Mis õigused on süsteemi kasutajate rollidel? Selgitus: et realiseerida vastavad juurdepääsuõigused.</li>
		<li>Kas süsteem peab toetama väljaprintimise funktsionaalsust või kõik toimub ainult elektrooniliselt? Selgitus: et valida sobivad kujunduslahendused ning vajadusel realiseerida printimise funktsionaalsust.</li>
		<li>Kas süsteem peab logima kasutajate tegevusi? Selgitus: et saaks teostada vastavad logimis teenused.</li>
		
		<p></p>
		<h4>Mittefunktsionaalsed</h4>
		
		<li>Millal süsteem peab valmis olema? Selgitus: et hinnata ajalised ja ressursilised mahud.</li>
		<li>Kui suur on projekti eelarve? Selgitus: et hinnata projekti teostatavust ja ressursside piisavust.</li>
		<li>Kes hakkab kasutama süsteemi? Selgitus: et aru saada, kes on lõppkasutajad ja vastavalt projekteerida kasutajaliides.</li>
		<li>Mis keeles hakatakse süsteemi kasutama? Selgitus: et teostada kasutajaliides vajalikus keeles.</li>
		<li>Millised on projekti turvalisuse nõuded? Selgitus: et hinnata missuguseid turvameetmeid peab süsteemi realiseerima.</li>
		<li>Milline peab olema süsteemi uptime (või maksimaalne downtime)? Selgitus: et projekteerimisel arvestada sellega ja töötada välja vastavad taastamismeetmed.</li>
		<li>Kus hakatakse kasutama süsteemi? Selgitus: et selgitada mis on ühendamisviisid.</li>
		<li>Mis seadmetega hakatakse süsteemi kasutama? Selgitus: et selgitada kas kasutajaliides peab olema universaalne või seda tuleb teha ainult ühe seadmetüübi jaoks.</li>
		<li>Kas süsteem peab olema hästi skaleeruv? Selgitus: et valida sobilik lahendus.</li>
		<li>Kui pikkalt peab salvestama andmeid? Selgitus: et selgitada mahulisi piirangud.</li>
		<li>Kas peale süsteemi üleandmist on vajadus meiepoolsele toele? Selgitus: et varuda piisavalt inimressurssi peale süsteemi üleandmist.</li>
		<li>Kes on tellija esindajad ja mis õigused neil on? Selgitus: et saada aru kellega mis küsimusi arutleda ja kes on lõplik otsustaja nendes küsimustes.</li>
		<li>Kas süsteem peab võimaldama kahte paralleelselt toimivat toimingut ühe andmekogumiga? Selgitus: et teostada vastav funktsionaalsus.</li>
	</ol>

	<h2>Kasutuslood</h2>

	<ol>
		<li>Konsultandina soovin luua uusi konkursse/projekte.</li>
		<li>Konsultandina soovin vaadata kandideerijate nimekirja.</li>
		<li>Konsultandina soovin otsida inimeste ankeete et vaadata neid.</li>
		<li>Konsultandina soovin määrata inimestele testid.</li>
		<li>Konsultandina soovin võrrelda inimeste ankeete, testide tulemsui.</li>
		<li>Konsultandina soovin koostada sobilike kandidaatide nimekirja, et administratiivtöötaja teaks kellega intervjuu aegu leppida.</li>
		<li>Konsultandina soovin näha kalendris minu poolt läbiviidavad intervjuud, et valmistada ette ja planeerida oma aega.</li>
		<li>Konsultandina soovin sulgeda konkurssi, et vastav teade oleks edastatud raamatupidajale kes koostab arvet.</li>
		<li>Konsultandina soovin otsida klient-ettevõtteid, et vaadata nende infot. </li>
		<li>Konsultandina soovin koostada töökuulutusi ning määrama kus ja kui kauaks nad peavad olema esitatud.</li>
		
		<p></p>
		
		<li>Administratiivtöötajana soovin lisada kandidaatide ankeete, kes soovisid kandideerida e-posti teel (või muul viisil).</li>
		<li>Administratiivtöötajana soovin näha sobilike kandidaatide kontkatandmed, et leppida nendega aega intervjuuks.</li>
		<li>Administratiivtöötajana soovin lisada kalendrisse intervjuu eest vastutava konsultandi, toimimisaegu ja -kohti (kas füüsiline ruum, telefon, Skype jms).</li>
		<li>Administratiivtöötajana soovin broneerida ruume intervjuude jaoks.</li>
		<li>Administratiivtöötajana soovin lisada konkurssidele töökuulutusi, mis lähevad meediasse (pilt, PDF, tekst mis lähevad ajalehesse, Facebook-i jne).</li>
		<li>Administratiivtöötjana soovin lisada kalendrisse meedias olevaid kuulutusi ja nende kehtivusaegu, ka konkursside kaupa. Et omada ülevaadet, kas mõni kuulutus vajaks värskendamist.</li> 
		<li>Administratiivtöötajana soovin näha mulle määratud toimingute nimekirja (kuulutuse lisamine, kuulutuse värskendamine, kandidaadi vastu võtmine ja juhatamine kas konsultandini või testiruumine jne).</li>
		
		<p></p>
		
		<li>Kandidaadina soovin veebilehe kaudu sisestada oma ankeeti.</li>
		<li>Kandidaadina soovin veebilehe kaudu kandideerida konkursile.</li>
		<li>Kandidaadina soovin teha konsultandi poolt määratud testi.</li>
	</ol>

