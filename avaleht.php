	<h2>Projekt "Personaliotsingu firma uue CRM-i projekteerimine".</h2>
	
	<h3>Töö kirjeldus</h3>
	
	<p>Tellija on personaliotsingu firma. Ülesanneks on projekteerida uus CRM (Customer relationship management), kuna vana on tehtud vanadel 
	tehnoloogiatel, halvasti dokumenteeritud ning selle ümbertegemine/täiustamine ei ole otstarbekas. CRM peab võimladama:</p>
	
	<ul>
		<li>Hallata klientide andmed</li>
		<li>Hallata kandidaatide andmed</li>
		<li>Hallata konkursse</li>
		<li>Kasutada paindlike otsingufiltreid</li>
		<li>Integreerimist olemasoleva veebilehega</li>
	</ul>
	
	<p>CRM peab olema paindlik ja lihtsasti kasutatav.</p>
	
	<p>Projekti teostajate vastutus on koostada CRM-i projekt, mille järgi saab arendada uue CRM-i.</p>
	
	<p>Kuna tegemist on kooliprojektiga, siis selles töös ei ole kajastatud kogu projekt: nõuete/klaside/jne hulk on piiratud.</p>
	
	<p>Töös ei ole kirjeldatud migreerimist vanalt CRM-lt uuele. Tehakse eeldus, et andmete migreerimine toimub andmebaasi importimise teel.</p>
	
